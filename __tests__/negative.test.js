const test = require('tape')
const checker = require('../')

test('negative() tests', t => {
  t.plan(17)

  t.ok(checker.negative(-1), 'negative(-1) should be ok')
  t.ok(checker.negative(-10), 'negative(-10) should be ok')
  t.ok(checker.negative(-19.540), 'negative(-19.540) should be ok')
  t.ok(checker.negative('-1009.0055'), 'negative(\'-1009.0055\') should be ok')
  t.notOk(checker.negative(1000.01), 'negative(1000.01) should be falsy')
  t.notOk(checker.negative(+1), 'negative(+1) should be falsy')
  t.notOk(checker.negative('0'), 'negative(\'0\') should be falsy')
  t.notOk(checker.negative(0), 'negative(0) should be falsy')
  t.notOk(checker.negative(null), 'negative(null) should be falsy')
  t.notOk(checker.negative(false), 'negative(false) should be falsy')
  t.notOk(checker.negative(true), 'negative(true) should be falsy')
  t.notOk(checker.negative(), 'negative() should be falsy')
  t.notOk(checker.negative(''), 'negative(\'\') should be falsy')
  t.notOk(checker.negative('abc'), 'negative(\'abc\') should be falsy')
  t.notOk(checker.negative([]), 'negative([]) should be falsy')
  t.notOk(checker.negative({}), 'negative({}) should be falsy')
  t.notOk(checker.negative('\n\r\t'), 'negative(\'\n\r\t\') should be falsy')
})
